funcion = get(handles.menu,'Value');
set(handles.axes1,'Visible','on');
switch funcion
    case 1
       axis(handles.axes1);
       x=0:xlimite/1000:xlimite;
       y=sin(x);
       plot(x,y);
       axis([0 xlimite -1 ylimite]);
    case 2
       axis(handles.axes1);
       x=0:xlimite/1000:xlimite;
       y=cos(x);
       plot(x,y);
       axis([0 xlimite -1 ylimite]);
    case 3
       axis(handles.axes1);
       x=0:xlimite/1000:xlimite;
       y=tan(x);
       plot(x,y);
       axis([0 xlimite -1 ylimite]); 
    case 4
       axis(handles.axes1);
       x=0:xlimite/1000:xlimite;
       y=sin(x);
       z=cos(x);
       plot3(x,y,z);
       axis([0 xlimite -1 ylimite]); 
end
